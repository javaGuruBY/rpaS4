
public class MathOperationsDemo {
    public static void main(String[] args) {
        System.out.println("Welcome to the 'Math Operations'!");


        int aAdd = 235;
        int bAdd = 65;

        System.out.println("1) Addition '+':");
        System.out.println("a = " + aAdd + ", b = " + bAdd);
        int sum = aAdd + bAdd;
        System.out.println("a + b = " + aAdd + " + " + bAdd + " = " + sum);


        int aSub = 235;
        int bSub = 65;

        System.out.println("2) Difference '-':");
        System.out.println("a = " + aSub + ", b = " +bSub);
        int sub = aSub - bSub;
        System.out.println("a - b = " + aSub + " - " + bSub + " = " + sub);


        int aMult = 36;
        int bMult = 65;

        System.out.println("3) Multiplication '*':");
        System.out.println("a = " + aMult + ", b = " + bMult);
        int mult = aMult * bMult;
        System.out.println("a * b = " + aMult + " * " + bMult + " = " + mult);



        int aDiv = 235;
        int bDiv = 5;

        System.out.println("4) Division '/':");
        System.out.println("a = " + aDiv + ", b = " + bDiv);
        int div = aDiv / bDiv;
        System.out.println("a / b = " + aDiv + " / " + bDiv + " = " + div);



    }
}
